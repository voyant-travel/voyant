/**
 * Keeps the @voyant-travel/framework BOM in sync with the runtime-module set.
 * RFC: docs/architecture/consolidated-deployments-rfc.md (Workstream A).
 *
 * The BOM pins exactly the mounted runtime modules (the same set the lockstep
 * membership checker emits to release.runtime-packages.generated.json), as
 * `workspace:*` deps — pnpm publishes those as the EXACT current version, so the
 * published BOM is a deterministic, tested set. A deployment depends on the one
 * BOM version; `voyant upgrade` bumps it. Only the BOM + actually-changed
 * packages republish, so there's no per-package email spam.
 *
 * This generator rewrites the BOM's `dependencies` + a generated source list
 * from release.runtime-packages.generated.json. `--emit` writes; default mode
 * fails on drift (so CI catches a runtime module added/removed without
 * regenerating the BOM).
 */
import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs"
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, "..")
const MANIFEST = join(ROOT, "release.runtime-packages.generated.json")
const PKG = join(ROOT, "packages/framework/package.json")
const SRC = join(ROOT, "packages/framework/src/runtime-packages.generated.ts")
const CONTRIBUTORS = join(ROOT, "packages/framework/src/runtime-contributors.generated.ts")
const DISTRIBUTION = join(ROOT, "packages/framework/src/operator-distribution.ts")
const EMIT = process.argv.includes("--emit")

const runtimePackages = JSON.parse(readFileSync(MANIFEST, "utf-8")).runtimePackages
const standardPackages = [
  ...readFileSync(DISTRIBUTION, "utf8").matchAll(/resolve:\s*"(@voyant-travel\/[^"/]+)/g),
].map((match) => match[1])
const workspacePackageNames = readWorkspacePackageNames()
const manifestReferencePackages = findManifestFiles(join(ROOT, "packages"))
  .flatMap((path) => [
    ...readFileSync(join(ROOT, "packages", path), "utf8").matchAll(
      /["'](@voyant-travel\/[^"'/]+)(?:\/[^"']*)?["']/g,
    ),
  ])
  .map((match) => match[1])
  .filter((name) => workspacePackageNames.has(name))
const bomPackages = [
  ...new Set([...runtimePackages, ...standardPackages, ...manifestReferencePackages]),
].sort()
const runtimeContributors = bomPackages
  .flatMap((packageName) => {
    const packageJsonPath = findWorkspacePackageJson(packageName)
    if (!packageJsonPath) return []
    const runtime = JSON.parse(readFileSync(packageJsonPath, "utf8")).voyant?.runtime
    if (
      typeof runtime?.entry !== "string" ||
      !runtime.entry.startsWith("./") ||
      typeof runtime.export !== "string"
    ) {
      return []
    }
    const entry = runtime.entry === "./" ? packageName : `${packageName}/${runtime.entry.slice(2)}`
    return [{ packageName, entry, exportName: runtime.export }]
  })
  .sort((left, right) => left.packageName.localeCompare(right.packageName))
const frameworkRuntimeEntryDeps = {
  "@voyant-travel/auth": "workspace:*",
  "@voyant-travel/cloud-sdk": "^0.11.0",
  "@voyant-travel/connect-sdk": "0.9.1",
  "@voyant-travel/core": "workspace:*",
  "@voyant-travel/cruises": "workspace:*",
  "@voyant-travel/db": "workspace:*",
  "@voyant-travel/framework-migrations": "workspace:*",
  "@voyant-travel/mcp": "workspace:*",
  "@voyant-travel/plugin-voyant-connect": "^0.3.2",
  "@voyant-travel/runtime": "workspace:*",
  "@voyant-travel/storage": "workspace:*",
  "@voyant-travel/tools": "workspace:*",
  "@voyant-travel/types": "workspace:*",
  "@voyant-travel/utils": "workspace:*",
  "@voyant-travel/workflows": "workspace:*",
  "@voyant-travel/workflows-orchestrator": "workspace:*",
  "drizzle-orm": "catalog:",
  pg: "^8.22.0",
  typescript: "catalog:",
}

// BOM dependencies include every standard product selection owner, not only
// packages with server runtime membership. Otherwise a minimal project cannot
// resolve manifest-only/admin-only selections without repeating dependencies.
const deps = {}
for (const name of bomPackages) deps[name] = "workspace:*"
for (const name of Object.keys(frameworkRuntimeEntryDeps).sort()) {
  deps[name] = frameworkRuntimeEntryDeps[name]
}

const pkg = JSON.parse(readFileSync(PKG, "utf-8"))
const nextPkg = `${JSON.stringify({ ...pkg, dependencies: deps }, null, 2)}\n`

const srcBody = `// GENERATED by scripts/generate-framework-bom.mjs — do not edit.
// The runtime-module set this framework version pins (the BOM membership).
// Refresh with \`node scripts/generate-framework-bom.mjs --emit\`.

export const FRAMEWORK_RUNTIME_PACKAGES = [
${[...runtimePackages]
  .sort()
  .map((n) => `  "${n}",`)
  .join("\n")}
] as const

export const FRAMEWORK_STANDARD_PACKAGES = [
${bomPackages.map((name) => `  "${name}",`).join("\n")}
] as const

export type FrameworkRuntimePackage = (typeof FRAMEWORK_RUNTIME_PACKAGES)[number]
`

const contributorBody = `// GENERATED by scripts/generate-framework-bom.mjs — do not edit.
// This barrel keeps generated project artifacts resolvable through their direct framework dependency.
// Package manifests remain the authority for contributor entrypoints and export names.

${runtimeContributors
  .map(({ entry, exportName }) => `export { ${exportName} } from ${JSON.stringify(entry)}`)
  .join("\n")}
`

const violations = []
if (EMIT) {
  writeFileSync(PKG, nextPkg)
  writeFileSync(SRC, srcBody)
  writeFileSync(CONTRIBUTORS, contributorBody)
  console.log(`generate-framework-bom: emitted ${bomPackages.length} standard product deps`)
} else {
  if (!existsSync(SRC) || readFileSync(SRC, "utf-8") !== srcBody) {
    violations.push("packages/framework/src/runtime-packages.generated.ts is stale")
  }
  if (!existsSync(CONTRIBUTORS) || readFileSync(CONTRIBUTORS, "utf-8") !== contributorBody) {
    violations.push("packages/framework/src/runtime-contributors.generated.ts is stale")
  }
  if (readFileSync(PKG, "utf-8") !== nextPkg) {
    violations.push("packages/framework/package.json dependencies are stale")
  }
  if (violations.length) {
    console.error("Framework BOM drift — run `node scripts/generate-framework-bom.mjs --emit`:")
    for (const v of violations) console.error(`  - ${v}`)
    process.exit(1)
  }
  console.log(`check-framework-bom: OK (${bomPackages.length} standard product deps)`)
}

function findWorkspacePackageJson(packageName) {
  for (const root of [join(ROOT, "packages"), join(ROOT, "packages", "plugins")]) {
    for (const entry of readdirSync(root)) {
      const packageJson = join(root, entry, "package.json")
      if (!existsSync(packageJson)) continue
      if (JSON.parse(readFileSync(packageJson, "utf8")).name === packageName) return packageJson
    }
  }
  return undefined
}

function readWorkspacePackageNames() {
  const names = new Set()
  for (const root of [join(ROOT, "packages"), join(ROOT, "packages", "plugins")]) {
    for (const entry of readdirSync(root)) {
      const directory = join(root, entry)
      if (!statSync(directory).isDirectory()) continue
      const packageJson = join(directory, "package.json")
      if (!existsSync(packageJson)) continue
      const name = JSON.parse(readFileSync(packageJson, "utf8")).name
      if (name) names.add(name)
    }
  }
  return names
}

function findManifestFiles(directory, relative = "") {
  const files = []
  for (const entry of readdirSync(directory, { withFileTypes: true })) {
    if (["node_modules", "dist", "coverage", ".turbo"].includes(entry.name)) continue
    const nextRelative = join(relative, entry.name)
    const path = join(directory, entry.name)
    if (entry.isDirectory()) files.push(...findManifestFiles(path, nextRelative))
    else if (/^(?:voyant|graph-manifest)\.ts$/.test(entry.name)) files.push(nextRelative)
  }
  return files
}
