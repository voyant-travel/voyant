import assert from "node:assert/strict"
import { describe, it } from "node:test"

import { buildCiRepairDrillReport } from "../lib/agent-runner-ci-repair-drill.mjs"

describe("agent runner CI repair drill", () => {
  it("proves the CI repair shepherd state transitions without mutating GitHub", () => {
    const report = buildCiRepairDrillReport({
      date: new Date("2026-05-12T12:00:00.000Z"),
      repoRoot: "/repo",
      repository: "voyant-travel/voyant",
    })

    assert.deepEqual(
      report.steps.map((step) => [
        step.name,
        step.result?.agentState ?? step.values["Agent State"],
      ]),
      [
        ["sync failing PR", "CI Repair"],
        ["recommend CI evidence collection", "CI Repair"],
        ["collect CI repair packet", "CI Repair"],
        ["recommend narrow repair command", "CI Repair"],
        ["run narrow repair command", "Human Review"],
        ["sync green PR", "Merge Ready"],
      ],
    )
    assert.equal(report.steps[1].recommendation, "collect-ci")
    assert.equal(report.steps[3].recommendation, "run-command")
    assert.equal(
      report.commandEnvironment.ciRepairEvidenceReference,
      ".agent-runs/579-validate-ci-repair-shepherd-loop/ci-repair-2026-05-12T12-00-00-000Z.md",
    )
    assert.match(report.ciRepairEvidence.preview, /Synthetic failing test log/)
    assert.doesNotMatch(report.ciRepairEvidence.preview, /codex|claude|generated by/i)
  })
})
