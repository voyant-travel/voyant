#!/usr/bin/env node

import { existsSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from "node:fs"
import { dirname, join, relative, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..")
const operatorRoot = join(repoRoot, "starters/operator")
const outputRoot = join(operatorRoot, ".voyant")

export const OPERATOR_GENERATED_METADATA_FILES = [
  "env.d.ts",
  "tsconfig.client.json",
  "tsconfig.server.json",
  "tsconfig.tests-smoke.json",
  "vite.config.ts",
  "vitest.config.ts",
]
const RETIRED_OPERATOR_GENERATED_FILES = [
  "admin/selected-graph-admin.generated.ts",
  "runtime-entry.generated.ts",
  "runtime/graph-runtime.generated.ts",
]

export function buildOperatorStarterMetadata(root = repoRoot) {
  const appRoot = join(root, "starters/operator")
  const generatedRoot = join(appRoot, ".voyant")
  const paths = declarationPaths(root, generatedRoot)
  const baseCompilerOptions = {
    target: "ES2022",
    jsx: "react-jsx",
    module: "ESNext",
    lib: ["ES2022", "DOM", "DOM.Iterable"],
    types: ["./env.d.ts", "node", "vite/client"],
    moduleResolution: "bundler",
    allowImportingTsExtensions: true,
    verbatimModuleSyntax: true,
    noEmit: true,
    skipLibCheck: true,
    strict: true,
    noUnusedLocals: true,
    noUnusedParameters: true,
    noFallthroughCasesInSwitch: true,
    noUncheckedSideEffectImports: true,
  }
  const serverFiles = ["server.ts", "start.ts"]

  return new Map([
    [
      "env.d.ts",
      `/** Generated by scripts/generate-operator-starter-metadata.mjs. */
type AppBindings = import("@voyant-travel/framework/node-runtime").VoyantNodeRuntimeEnv
`,
    ],
    [
      "tsconfig.client.json",
      json({
        compilerOptions: { ...baseCompilerOptions, paths },
        include: [
          "../src/**/*.tsx",
          "../src/**/*.ts",
          "../tests/selected-graph-*-admin.test.ts",
          "./**/*.ts",
          "./**/*.tsx",
        ],
        exclude: [
          "../node_modules",
          "../src/api/**",
          "../src/modules/**",
          "../src/links/**",
          "../src/extensions/**",
          "../src/custom-fields/**",
          ...serverFiles.map((file) => `../src/${file}`),
          "./runtime/**",
          "./runtime-entry.generated.ts",
        ],
      }),
    ],
    [
      "tsconfig.server.json",
      json({
        compilerOptions: { ...baseCompilerOptions, paths },
        include: [
          ...serverFiles.map((file) => `../src/${file}`),
          "../src/api/**/*.ts",
          "../src/modules/**/*.ts",
          "../src/links/**/*.ts",
          "../src/extensions/**/*.ts",
          "../src/custom-fields/**/*.ts",
          "./vite.config.ts",
          "./vitest.config.ts",
        ],
        exclude: [
          "../node_modules",
          "../**/*.tsx",
          "../src/admin/**",
          "../src/components/**",
          "../src/routes/**",
          "../src/hooks/**",
          "../src/router.tsx",
          "../src/lib/admin-i18n.ts",
          "./routes/**",
          "./routeTree.gen.ts",
          "./admin/**",
          "./runtime/**",
        ],
      }),
    ],
    [
      "tsconfig.tests-smoke.json",
      json({
        compilerOptions: { ...baseCompilerOptions, paths },
        include: [
          "../tests/api/action-ledger-schema-mount.test.ts",
          "../tests/api/operator-route-mounting.test.ts",
          "../tests/api/selected-catalog-runtime.test.ts",
          "../tests/voyant.config.test.ts",
        ],
        exclude: ["../node_modules", "./runtime/**"],
      }),
    ],
    ["vite.config.ts", viteConfigSource()],
    ["vitest.config.ts", vitestConfigSource()],
  ])
}

export function writeOperatorStarterMetadata(root = repoRoot, { check = false } = {}) {
  const generatedRoot = join(root, "starters/operator/.voyant")
  const stale = []
  for (const path of unexpectedMetadataFiles(generatedRoot)) {
    stale.push(`unexpected:${path}`)
    if (!check) rmSync(join(generatedRoot, path))
  }
  for (const [path, contents] of buildOperatorStarterMetadata(root)) {
    const destination = join(generatedRoot, path)
    if (existsSync(destination) && readFileSync(destination, "utf8") === contents) continue
    stale.push(path)
    if (!check) {
      mkdirSync(dirname(destination), { recursive: true })
      writeFileSync(destination, contents)
    }
  }
  return stale
}

function unexpectedMetadataFiles(generatedRoot) {
  if (!existsSync(generatedRoot)) return []
  const expected = new Set(OPERATOR_GENERATED_METADATA_FILES)
  const unexpected = readdirSync(generatedRoot).filter(
    (path) =>
      !expected.has(path) &&
      /^(?:env\.d\.ts|tsconfig(?:\.[^.]+)?\.json|turbo\.json|vite\.config\.ts|vitest\.config\.ts)$/.test(
        path,
      ),
  )
  return [
    ...unexpected,
    ...RETIRED_OPERATOR_GENERATED_FILES.filter((path) => existsSync(join(generatedRoot, path))),
  ].sort()
}

function declarationPaths(root, generatedRoot) {
  const sourcePath = join(root, "packages/typescript-config/dep-paths.json")
  const sourceRoot = dirname(sourcePath)
  const source = JSON.parse(readFileSync(sourcePath, "utf8")).compilerOptions.paths
  const paths = { "@/*": ["../src/*"] }
  for (const [specifier, targets] of Object.entries(source)) {
    paths[specifier] = targets.map((target) => {
      const rebased = relative(generatedRoot, resolve(sourceRoot, target)).replaceAll("\\", "/")
      return rebased.startsWith(".") ? rebased : `./${rebased}`
    })
  }
  return paths
}

function viteConfigSource() {
  return `import tailwindcss from "@tailwindcss/vite"
import { devtools } from "@tanstack/devtools-vite"
import { tanstackStart } from "@tanstack/react-start/plugin/vite"
import viteReact from "@vitejs/plugin-react"
import { standardOperatorRouteFiles } from "@voyant-travel/operator-standard/standard-route-files"
import {
  createAnalyzePlugin,
  VOYANT_ROUTE_FILE_IGNORE_PATTERN,
  voyantGeneratedRoutes,
  voyantStartViteConfig,
} from "@voyant-travel/vite-config"
import { defineConfig } from "vite"

const appRootUrl = new URL("../generated-config-anchor.ts", import.meta.url).href
const generatedRoutes = voyantGeneratedRoutes({
  appRootUrl,
  files: standardOperatorRouteFiles,
})

export default defineConfig(
  voyantStartViteConfig({
    appRootUrl,
    nodeSsr: true,
    plugins: [
      generatedRoutes.plugin,
      devtools(),
      tailwindcss(),
      tanstackStart({
        router: {
          routesDirectory: generatedRoutes.routesDirectory,
          generatedRouteTree: generatedRoutes.generatedRouteTree,
          routeFileIgnorePattern: VOYANT_ROUTE_FILE_IGNORE_PATTERN,
        },
      }),
      viteReact(),
      createAnalyzePlugin(import.meta.url),
    ],
  }),
)
`
}

